    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        String nome = txtNome.getText();
        String dataNascimentoStr = txtDataNascimento.getText();
        String email = txtEmail.getText();
        String cpf = txtCpf.getText();
        String endereco = txtEndereco.getText();
        String telefone = txtTelefone.getText();
        
        if (nome.isBlank() || dataNascimentoStr.isBlank() || email.isBlank() ||
            cpf.isBlank() || endereco.isBlank() || telefone.isBlank()) {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos");
            return;
        }
        
        Date dataNascimento = null;
        try {
            SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
            dataNascimento = sdf.parse(dataNascimentoStr);
        } catch (ParseException e) {
            JOptionPane.showMessageDialog(null, "Data de nascimento invÃ¡lida. Use o formato dd/MM/yyyy.");
            return;
        }
        
        Cliente clienteEditado = new Cliente();
        
        clienteEditado.setId(this.cliente.getId());
        clienteEditado.setNome(nome);
        clienteEditado.setDataNascimento(dataNascimento);
        clienteEditado.setEmail(email);
        clienteEditado.setCpf(cpf);
        clienteEditado.setEndereco(endereco);
        clienteEditado.setTelefone(telefone);
        
        Utils utils = new Utils();
        int opcao = utils.confirmar("Deseja mesmo editar esse cliente?");

        if (opcao == JOptionPane.YES_OPTION) {
            ClienteDAO clienteDAO = new ClienteDAO();
            clienteDAO.editar(clienteEditado);
        }
    }//GEN-LAST:event_btnSalvarActionPerformed

