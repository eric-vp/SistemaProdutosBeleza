    private void preencherTabela(List<Fornecedor> listaFornecedores) {        
        String colunas[] = {"id", "nome", "cnpj", "telefone"};
        Object[][] dados = new Object[listaFornecedores.size()][colunas.length];
        
        int i = 0;
        for (Fornecedor f : listaFornecedores) {
            dados[i] = new Object[] {
                f.getId(),
                f.getNome(),
                f.getCnpj(),
                f.getTelefone()
            };
            i++;
        }
        DefaultTableModel tabelaFornecedores = new DefaultTableModel(dados, colunas);
        tblFornecedores.setModel(tabelaFornecedores);
    }
    
