    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        String pesquisa = txtPesquisar.getText();        
        FornecedorDAO fornecedorDAO = new FornecedorDAO();
        
        if(pesquisa.isBlank()){
            return;
        }
        
        preencherTabela(fornecedorDAO.pesquisar(pesquisa));
    }//GEN-LAST:event_btnPesquisarActionPerformed

    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        Utils utils = new Utils();
        
        int posicao = utils.getLinhaSelecionada(tblFornecedores, "um fornecedor");
        
        if (posicao >= 0) {
            DefaultTableModel tabela = (DefaultTableModel) tblFornecedores.getModel();
            int id = (int) tabela.getValueAt(posicao, 0);
            String nome = (String) tabela.getValueAt(posicao, 1);
            String cnpj = (String) tabela.getValueAt(posicao, 2);
            String telefone = (String) tabela.getValueAt(posicao, 3);            
            
            Fornecedor fornecedor = new Fornecedor();
            fornecedor.setId(id);
            fornecedor.setNome(nome);
            fornecedor.setCnpj(cnpj);
            fornecedor.setTelefone(telefone);
            
            EditarFornecedor editarFornecedor = new EditarFornecedor(fornecedor);
            editarFornecedor.setVisible(true);
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        Utils utils = new Utils();
        FornecedorDAO fornecedorDAO = new FornecedorDAO();
        
        int posicao = utils.getLinhaSelecionada(tblFornecedores, "um fornecedor");
        
        if (posicao >= 0) {
            DefaultTableModel tabela = (DefaultTableModel) tblFornecedores.getModel();
            
            int id = (int) tabela.getValueAt(posicao, 0);
            
            Fornecedor fornecedor = new Fornecedor();
            fornecedor.setId(id);
            int opcao = utils.confirmar("Deseja mesmo excluir esse fornecedor?");

            if (opcao == JOptionPane.YES_OPTION) {
                fornecedorDAO.excluir(fornecedor);
            }
        }
        
        preencherTabela(fornecedorDAO.listar());
    }//GEN-LAST:event_btnExcluirActionPerformed

