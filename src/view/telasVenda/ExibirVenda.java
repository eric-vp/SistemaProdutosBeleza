    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        Utils utils = new Utils();
        
        int posicao = utils.getLinhaSelecionada(tblVendas, "uma venda");
        
        if (posicao >= 0) {
            try {
                DefaultTableModel tabela = (DefaultTableModel) tblVendas.getModel();
                int id = (int) tabela.getValueAt(posicao, 0);
                String status = tabela.getValueAt(posicao, 1).toString();
                int clienteId = Integer.parseInt(tabela.getValueAt(posicao, 2).toString());
                java.sql.Date dataSql = (java.sql.Date) tabela.getValueAt(posicao, 3);       
                Double valorTotal = Double.parseDouble(tabela.getValueAt(posicao, 4).toString());

                Venda venda = new Venda();
                venda.setId(id);
                venda.setStatus(status);
                venda.setClienteId(clienteId);
                venda.setData(dataSql);
                venda.setTotalPagamento(valorTotal);

                EditarVenda editarVenda = new EditarVenda(venda);
                editarVenda.setVisible(true);
                
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(null, "Erro ao converter valores numÃ©ricos da tabela.");
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Erro ao carregar os dados do produto: " + e.getMessage());
            }
        }
    }//GEN-LAST:event_btnEditarActionPerformed

    private void btnExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnExcluirActionPerformed
        Utils utils = new Utils();
        VendaDAO vendaDAO = new VendaDAO();
        
        int posicao = utils.getLinhaSelecionada(tblVendas, "uma venda");
        
        if (posicao >= 0) {
            DefaultTableModel tabela = (DefaultTableModel) tblVendas.getModel();
            
            int id = (int) tabela.getValueAt(posicao, 0);
            
            Venda venda = new Venda();
            venda.setId(id);
            int opcao = utils.confirmar("Deseja mesmo excluir essa venda?");

            if (opcao == JOptionPane.YES_OPTION) {
                vendaDAO.excluir(venda);
            }
        }
        
        preencherTabela(vendaDAO.listar());
    }//GEN-LAST:event_btnExcluirActionPerformed

