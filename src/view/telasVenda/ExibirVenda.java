    private void preencherTabela(List<Venda> listaVendas) {        
        String colunas[] = {"id", "status", "Id cliente", "data", "valor total"};
        Object[][] dados = new Object[listaVendas.size()][colunas.length];
        
        int i = 0;
        for (Venda v : listaVendas) {
            dados[i] = new Object[] {
                v.getId(),
                v.getStatus(),
                v.getClienteId(),
                v.getData(),
                v.getTotalPagamento()
            };
            i++;
        }
        DefaultTableModel tabelaProdutos = new DefaultTableModel(dados, colunas);
        tblVendas.setModel(tabelaProdutos);
    }
